    <h2>Bilaga 6 — Utrustning för militära divisionerna</h2>
    <p>Tävlande i militärdivisionen skall minst medföra följande utrustning vid skjutstationens början:</p>
    <ol>
      <li>Den pistol som nyttjas i tävlingen, oladdad i hölstret. Om pistolen ej nyttjas under skjutstationen så får
        ett tomt magasin sitta i pistolen under skjutstationen såvida domaren (RO) medger det.</li>
      <li>Vid starten av en skjutstation ska minst 90 patroner till karbin/gevär i minst tre (3) magasin om 30 skott
        medföras. För vapen som nyttjar större patroner såsom 7,62×51 (ptr 10 m.fl.) är fyra (4) magasin om 20 skott
        tillräckligt.</li>
      <li>Vid starten av en skjutstation ska minst 30 patroner till pistol i minst två magasin medföras.</li>
      <li>Vid starten av en skjutstation ska minst en (1) liter vatten bäras.</li>
      <li>Kniv eller annat eggvapen. Bladlängden skall vara minst 9 cm.</li>
      <li>Ficklampa, vapenlampa eller annan typ av ficklampa.</li>
      <li>Vapenrem, fastsatt på vapnet eller medförd i buren utrustning.</li>
      <li>Sjukvårdsutrustning, minst bestående av en tourniquet och första förband (i internationella regler är
        nödfilt ett godkänt alternativ till första förband) som medförs i buren utrustning.</li>
      <li>Stridsväst, kroppsskydd med bäranordning eller stridsgördel.</li>
      <li>Handskar, lämpliga för fältbruk och i diskret färg. Burna eller medförda i buren utrustning.</li>
      <li>Klädsel lämpad för fältbruk.
        <ul>
          <li>Lämplig för tjänstebruk.</li>
          <li>Diskret färg.</li>
          <li>Långa ärmar och byxben.</li>
          <li>Vid Försvarsmaktstävling ska svensk fältuniform/stridsuniform bäras.</li>
        </ul>
      </li>
      <li>Kängor lämpade för fältbruk.
        <ul>
          <li>Lämpliga för tjänstebruk.</li>
          <li>Diskret färg.</li>
          <li>Skaften skall täcka fotknölarna.</li>
        </ul>
      </li>
    </ol>
    <p>Vikten på den tävlandes burna utrustning skall vara minst tolv (12) kg. I vägning ingår utrustning 1–9 enligt
      lista ovan samt:</p>
    <ul>
      <li>Utrustnings-/stridsbälte.</li>
      <li>Hjälm. All utrustning fast i hjälmen räknas med. Även hörselskydd och skyddsglasögon (som saknar skalmar)
        m.m. räknas in om skytten bär hjälm.</li>
      <li>All annan utrustning som medförs i skyttens burna utrustning, t.ex. i en ryggsäck eller väst (och som bärs
        under varje genomförande).</li>
      <li>Knä- och armbågsskydd.</li>
    </ul>
    <p>Avsiktlig lättnad av buren utrustning under en skjutstation är förbjuden.</p>
    <p>Den obligatoriska utrustningen 1–8 samt 10 enligt ovanstående lista måste kunna nås av den tävlande med minst
      en hand.</p>
    <p>Vid skjutstationer som innefattar skjutning med hagelgevär eller skarp-/prickskyttegevär (men inte
