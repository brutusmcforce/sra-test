    <a id="6" />
    <h2>6. Mål och Poäng</h2>

    <a id="6.1" />
    <h3>6.1 Allmänna principer</h3>
    <p id="6.1.1"><a href="#6.1.1"><span class="sk">6.1.1</span></a>
      De måltavlor (motsv.) som används bör representera sådana mål som eldhandvapnen i fråga kan antas nyttjas mot.
    </p>
    <p id="6.1.2"><a href="#6.1.2"><span class="sk">6.1.2</span></a>
      Två träffar räknas för varje måltavla om inte stationsbestämmelserna anger annat.
    </p>
    <p id="6.1.3"><a href="#6.1.3"><span class="sk">6.1.3</span></a>
      En enskild skjutstation bör inte nyttja fler än fem olika typer av mål.
    </p>
    <p id="6.1.4"><a href="#6.1.4"><span class="sk">6.1.4</span></a>
      Upp till 10 % av skjutstationens maximala poäng kan ges för olika typer av särskilda uppgifter (såsom att kasta
      en handgranat t.ex.).
    </p>
    <p id="6.1.5"><a href="#6.1.5"><span class="sk">6.1.5</span></a>
      Skjutstationens poäng beräknas från träffar i målen och poäng för eventuella särskilda uppgifter.
    </p>
    <p id="6.1.6"><a href="#6.1.6"><span class="sk">6.1.6</span></a>
      Ytterligare detaljerade instruktioner avseende mål och poänggivning finns i de tekniska bilagorna och i
      ”Riktlinjer för tävlingar”.
    </p>

    <a id="6.2" />
    <h3>6.2 Mål</h3>
    <p id="6.2.1"><a href="#6.2.1"><span class="sk">6.2.1</span></a>
      Lämpliga mål är måltavlor enligt bilaga 1, reaktiva mål, mål som nyttjas av Försvarsmakten, eller delar av dessa.
      Andra mål får också nyttjas om deras form och poänggivning görs känt för de tävlande.
    </p>
    <p id="6.2.2"><a href="#6.2.2"><span class="sk">6.2.2</span></a>
      Målen måste tydligt kunna urskiljas från bakgrunden och från varandra när de observeras från skjutplats,
      oberoende av väder och ljusförhållanden (under tävlingen). Mål som är på mer än 100 m avstånd skall förses med
      tydligt kontrasterande bakgrund vid nationella eller internationella mästerskap.
    </p>
    <p>
      Målens form och eventuell bild (t.ex. fototapet) skall följa god sed, och vara synliga och identifierbara för
      alla tävlande. Skjutstationer skall utvärdera skjutskicklighet, inte målupptäckt.
    </p>
    <p class="se-extension">
      <strong>Militära bestämmelser:</strong>
      Vid vissa tävlingar kan målupptäckt vara ett delmoment. Det ska då särskilt anges i tävlingsinbjudan att regel
      6.2.2 inte följs p.g.a. målupptäcktsmoment.
    </p>
    <p id="6.2.3"><a href="#6.2.3"><span class="sk">6.2.3</span></a>
      Reaktiva mål kan t.ex. vara fyrkanter och cirklar om 20–30 cm diameter, tillverkade av högkvalitetsstål (Hardox)
      för att motverka risk för rikoschetter. Målplattor kan också tillverkas av andra material (t.ex. gummi).
    </p>
    <p id="6.2.4"><a href="#6.2.4"><span class="sk">6.2.4</span></a>
      Reaktiva mål skall alltid falla eller gå sönder av en godtagbar träff (t.ex. en lerduva ska ha minst ett hål i
      sig), och de ska vara monterade och fastsatta på ett sådant sätt att de inte kan vridas i sidled. Förutom vid
      nationella och internationella mästerskap så får s.k. gong-mål nyttjas, alltså stålmål som då de träffas pendlar
      och/eller plingar eller på annat vis indikerar träffen. En tävlande kan be domaren att verifiera träffarna
      genom att anmäla ”TRÄFF!”.
    </p>
    <p id="6.2.5"><a href="#6.2.5"><span class="sk">6.2.5</span></a>
      Ett reaktivt mål kan anges vara ett ”blockerande mål”, vilket då måste falla eller gå sönder innan skytten får
      gå vidare till nästa mål. Det är inte möjligt att nyttja blockerande mål på skjutstationer som har ett begränsat
      antal skott (limited shots scoring).
    </p>
    <p id="6.2.6"><a href="#6.2.6"><span class="sk">6.2.6</span></a>
      Skjutavstånd till metallmål skall med anledning av risken för rikoschett och splitter vara minst 8 m för pistol,
      revolver och hagelgevär (med hagelpatron) respektive 20 m för karbin/gevär eller hagelgevär med slug/brenneke.
    </p>
    <p class="se-extension">
      <strong>Militära bestämmelser:</strong>
      Vid försvarsmaktstävlingar är SäkR styrande och minsta avstånd till stålmål är då 50 m om inte fragmenterande
      ammunition nyttjas vid aktuell skjutstation.
    </p>
    <p id="6.2.7"><a href="#6.2.7"><span class="sk">6.2.7</span></a>
      Reaktiva metallmål skall kalibreras med den typ av eldhandvapen som skall nyttjas på skjutstationen.
      Kalibreringsskottet avfyras från en skjutplats som är tillgänglig för den tävlande under genomförandet. På
      skjutstationer där skytten kan välja vapen så skall kalibreringsskottet skjutas med pistol om sådan får nyttjas
      av den tävlande på skjutstationen. Reaktiva mål måste falla om en den träffas av en fullständig träff inom
      träffområdet. Träffområdet är det område som är synligt från skjutplats. När det gäller stålmål av Popper-typ
      så kalibreras för träff i det runda området om inte annat anges i stationsbestämmelserna.
    </p>
    <p>
      Kalibrering av reaktiva mål med hagelgevär skall genomföras med en hagelladdning om max 28 g och från en pipa
      som inte är längre än 66 cm (26") försedd med cylinderchoke (ingen trångborrning).
    </p>
    <p id="6.2.8"><a href="#6.2.8"><span class="sk">6.2.8</span></a>
      Träffar på metallmål skall alltid målas innan nästa skytt skall starta, förutom hagelträffar eller plåtmål som
      är del i ett aktivt fallmålsystem (t.ex. ”SAAB-mål”, Fallmål 2010/2015/2020 o.s.v.). Vid tävlingar på lägre än
      nationell nivå bortses från denna regel.
    </p>
    <p id="6.2.9"><a href="#6.2.9"><span class="sk">6.2.9</span></a>
      ”Skjut-ej-mål” (no-shoot) skall vara i annan färg än mål som skall beskjutas, och skall vara tydligt uppmärkta
      med S/E eller N/S (i Finland och Estland E/A).
    </p>

    <a id="6.3" />
    <h3>6.3 Poängmarkering</h3>
    <p id="6.3.1"><a href="#6.3.1"><span class="sk">6.3.1</span></a>
      Markering genomförs av domaren (RO) som skall vara utrustad med en tolkskiva (scoring overlay/gauge card).
    </p>
    <p id="6.3.2"><a href="#6.3.2"><span class="sk">6.3.2</span></a>
      Mål får ej röras före markeringen sker. Om en tävlande rör sig närmare ett mål än en (1) m, och det är möjligt
      att vederbörande har påverkat målet före markering har skett, kan domaren (RO) fastställa att inga godtagbara
      träffar i det målet skall räknas.
    </p>
    <p id="6.3.3"><a href="#6.3.3"><span class="sk">6.3.3</span></a>
      I målet som beskrivs i bilaga 1 (metric-tavla) så räknas en träff som bryter poänglinjen som en träff. Om en
      sådan tavla kapas ned (motsv.) så måste en ny poänglinje ritas 5 mm in från snittet. Ett mål bakom skyl får
      inte kapas. På andra wellpappmål räknas ett hål som bryter in i den fastställda träffytan som en träff. Träff av
      splitter och andra fragment som uppenbart inte är en direkt träff av projektilen skall ej räknas som träff. En
      träff som skjutits genom skyl skall räknas som träff även om projektilen träffar med bredsida i målet (s.k.
      nyckelhål/tumling). Om målet är så pass skadat att markering inte kan ske, t.ex. för att det beskjutits på nära
      avstånd, skall den tävlande genomföra omskjutning (re-shoot) av skjutstationen.
    </p>
    <p id="6.3.4"><a href="#6.3.4"><span class="sk">6.3.4</span></a>
      I vridmål räknas inte hål som är större än dubbla kuldiametern som träff.
    </p>
    <p id="6.3.5"><a href="#6.3.5"><span class="sk">6.3.5</span></a>
      Käppar (läkt el. motsvarande) som endast håller upp mål eller regnskydd för mål är genomskjutningsbara. Alla mål
      och andra konstruktioner (kulisser etc.) på skjutstationen är ej genomskjutningsbara såvida inte annat angivits
      i stationsbestämmelserna.
    </p>
    <p id="6.3.6"><a href="#6.3.6"><span class="sk">6.3.6</span></a>
      Om samma projektil träffar två mål och hålet i det närmsta målet är helt och hållet i en poängzon, utan att
      bryta poänglinjen (ut från målet), så skall endast det närmsta målet räknas som träffat. Om det närmsta målet är
      genomskjutningsbart så räknas det bortre målet också som träffat. Om hålet i det närmsta målet bryter poänglinjen
      (ut från målet) så räknas båda målen som träffade.
    </p>
    <p id="6.3.7"><a href="#6.3.7"><span class="sk">6.3.7</span></a>
      En träff som bryter linjen mellan två poängzoner räknas som träff i den högre poängzonen.
    </p>
    <p id="6.3.8"><a href="#6.3.8"><span class="sk">6.3.8</span></a>
      Om den tävlande protesterar mot markeringen så skall målet bedömas av banchefen (RM), vars beslut är slutgiltigt.
    </p>
    <p id="6.3.9"><a href="#6.3.9"><span class="sk">6.3.9</span></a>
      Om den tävlande protesterar mot markeringen av ett reaktivt mål som beskjutits med hagelgevär så kan denne
      begära att kalibrering skall ske omedelbart efter genomförandet.
    </p>
    <p>
      Om målet indikerar träff då det beskjuts (med kalibreringsskottet) så bedöms den markering som skytten
      protesterar mot vara en bom/miss. Om målet inte indikerar träff då det beskjuts skall skjutstationen skjutas om
      och banchefen (RM) skall meddelas.
    </p>

    <a id="6.4" />
    <h3>6.4 Målens poäng</h3>
    <p id="6.4.1"><a href="#6.4.1"><span class="sk">6.4.1</span></a>
      Om målet som beskrivs i bilaga 1 (metric-tavla) nyttjas ger:
    </p>
    <ul>
      <li>A-zon och B-zon 5 poäng,</li>
      <li>C-zon 3 poäng och</li>
      <li>D-zon 1 poäng.</li>
    </ul>
    <p id="6.4.2"><a href="#6.4.2"><span class="sk">6.4.2</span></a>
      Vid särskiljning mellan tävlande med samma poängsumma vinner den som under tävlingen haft flest träffar i A-zon,
      därefter flest träffar i B-zon och därefter i C-zon.
    </p>
    <p id="6.4.3"><a href="#6.4.3"><span class="sk">6.4.3</span></a>
      Vid markering av mål räknas poäng genom att räkna de träffar med högst poäng. (Normalt räknas två träffar om
      inte annat anges i stationsbestämmelserna, se 6.1.2.)
    </p>
    <p id="6.4.4"><a href="#6.4.4"><span class="sk">6.4.4</span></a>
      En träff på ett fallande mål eller ett sönderfallande mål ger fem (5) eller tio (10) poäng. Blockerande mål ger
      alltid 10 poäng.
    </p>
    <p id="6.4.5"><a href="#6.4.5"><span class="sk">6.4.5</span></a>
      Poängzonerna på andra typer av mål kan ge tio (10), fem (5), tre (3) eller ett (1) poäng utefter vad som
      angivits.
    </p>
    <p class="se-extension">I Sverige nyttjas även 4 och 2 poäng enligt fastställande av FM.</p>
    <p id="6.4.6"><a href="#6.4.6"><span class="sk">6.4.6</span></a>
      Ytterligare uppgifter kan också generera poäng.
    </p>

    <a id="6.5" />
    <h3>6.5 Poängberäkningsmetoder</h3>
    <ol type="a">
      <li><strong>Obegränsad:</strong> Tiden och antalet skott på skjutstationen är inte begränsade.</li>
      <li><strong>Begränsad tid:</strong> Tiden för genomförandet är begränsad men antalet skott är obegränsade.
        Resultatet är summan av antalet poäng som markerats på målen. Denna metod används till exempel med uppdykande
        och försvinnande mål.</li>
      <li><strong>Begränsade skott:</strong> Antalet skott för genomförandet är begränsade, men tiden är obegränsad.</li>
      <li><strong>Begränsad tid och skott med poängkvot:</strong> Såväl tiden som antalet skott är begränsade med
        poängkvot beräknas som resultat. De tävlande placeras som vid obegränsad metod.</li>
    </ol>

    <a id="6.6" />
    <h3>6.6 Straffpoäng</h3>
    <p id="6.6.1"><a href="#6.6.1"><span class="sk">6.6.1</span></a>
      Om en tävlande inte slutför en skjutstation så räknas poäng och tid fram till dess. Missar och icke beskjutna
      mål ger straffpoäng i enlighet med reglerna.
    </p>
    <p id="6.6.2"><a href="#6.6.2"><span class="sk">6.6.2</span></a>
      Vad som ger upphov till procedurfel skall anges i förväg i stationsbestämmelserna och får inte ges för andra
      anledningar än som angivits där eller i dessa regler.
    </p>
    <p>
      Ett procedurfel är en handling som avviker från stationsbestämmelserna. Som straff ges tio (10) straffpoäng för
      varje sådant fel. Domaren (RO) skall fastställa antalet fel och anledningen till dessa. Detta protokollförs
      tydligt i resultathanteringssystemet.
    </p>
    <p>Procedurfel får utdömas för att inte ha beskjutit ett mål, givet att detta anges i stationsbestämmelserna.</p>
    <p>
      Om den tävlande nuddar marken utanför en spärrlinje under skjutning, och genom detta får en tydlig fördel, skall
      procedurfel utdömas för varje skott som avfyras under kontakt med marken utanför spärrlinjen. Om den tävlande
      inte får en tydlig fördel så utdöms ett procedurfel. Antalet procedurfel som utdöms får inte överstiga antalet
      tillgängliga poäng.
    </p>
    <p>
      Om den tävlande genar och då överträder en begränsningslinje skall ett procedurfel utdömas för varje skott som
      avfyras efter genvägen togs. Den tävlande kan återställa en genväg som tagits av misstag genom att backa tillbaka
      till den plats där genvägen påbörjades och fortsätta genomförandet av skjutstationen från den platsen.
    </p>
    <p id="6.6.3"><a href="#6.6.3"><span class="sk">6.6.3</span></a>
      Varje stav som faller från en Tiitinen-tunnel ger ett procedurfel.
    </p>
    <p id="6.6.4"><a href="#6.6.4"><span class="sk">6.6.4</span></a>
      Träff i ett ”skjut-ej”-mål (S/E) ger tio (10) straffpoäng. Maximalt två (2) träffar räknas för varje S/E-mål om
      inget annat angivits i stationsbestämmelserna.
    </p>
    <p id="6.6.5"><a href="#6.6.5"><span class="sk">6.6.5</span></a>
      Missar/bommar på försvinnande eller vridmål bestraffas ej.
    </p>
    <p id="6.6.6"><a href="#6.6.6"><span class="sk">6.6.6</span></a>
      Om ett fallande mål står kvar eller ett sönderfallande mål förblir helt utdöms straffpoäng motsvarande dubbla
      maximala poängen av en träff.
    </p>
    <p id="6.6.7"><a href="#6.6.7"><span class="sk">6.6.7</span></a>
      Provanläggning mot mer än ett mål före skjutstationens genomförande startar ger ett procedurfel.
    </p>
    <p id="6.6.8"><a href="#6.6.8"><span class="sk">6.6.8</span></a>
      Att nyttja fler än ett benstöd eller att lämna benstöd under genomförandet ger ett procedurfel.
    </p>
    <p id="6.6.9"><a href="#6.6.9"><span class="sk">6.6.9</span></a>
      Vid obegränsad metod bestraffas en bom/miss med straffpoäng motsvarande dubbla maximala poängen för en träff.
    </p>
    <p id="6.6.10"><a href="#6.6.10"><span class="sk">6.6.10</span></a>
      Vid begränsad tid och vid begränsad tid och skott bestraffas inte bommar/missar. Varje skott som avlossas efter
      tidsgränsen ger straffpoäng motsvarande maximala poängen för en träff. Extraskott markeras i en särskild kolumn
      i resultathanteringssystemet.
    </p>
    <p id="6.6.11"><a href="#6.6.11"><span class="sk">6.6.11</span></a>
      Vid begränsade skott ger en bom/miss eller ett extraskott straffpoäng motsvarande dubbla maximala poängen för
      en träff. Extraskott markeras i en särskild kolumn i resultathanteringssystemet / protokollet.
    </p>
    <p id="6.6.12"><a href="#6.6.12"><span class="sk">6.6.12</span></a>
      Om ett blockerande mål inte fälls eller skjuts sönder av den tävlande utdöms ett procedurfel. Om felet ger
      tydlig fördel för den tävlande så utdöms ett procedurfel för varje skott som därefter skjuts.
    </p>
    <p id="6.6.13"><a href="#6.6.13"><span class="sk">6.6.13</span></a>
      Om en tävlande begär det så kan banchefen (RM) bestämma ett särskilt avdrag från en skjutstations poäng om en
      tävlande med anledning av fysisk skada el. dyl. begränsning är oförmögen att genomföra skjutstationen i enlighet
      med stationsbestämmelserna. Om banchefen (RM) accepterar begäran skall denne definiera ett avdrag om 1–20 % av
      skjutstationens poäng innan den tävlande påbörjar skjutstationen. Avdraget utförs genom erforderligt antal
      procedurfel. Banchefen (RM) kan också besluta att inte dra av några poäng om den tävlande har en särskilt
      omfattande fysisk begränsning. Om banchefen (RM) nekar begäran så tas bestraffningar med i resultatet som
      normalt.
    </p>
    <p id="6.6.14"><a href="#6.6.14"><span class="sk">6.6.14</span></a>
      Vid alla beräkningsmetoder kan en maximal tid fastställas för skjutstationens genomförande. Härvid rekommenderas
      180 sekunder. För varje skott efter tidsgränsen dras maximala poängen för en träff av från resultatet. Extraskott
      markeras i en särskild kolumn i resultathanteringssystemet. Domaren (RO) äger rätt att avbryta genomförandet då
      tidsgränsen löpt ut.
    </p>

    <a id="6.7" />
    <h3>6.7 Tävlingsresultat</h3>
    <p id="6.7.1"><a href="#6.7.1"><span class="sk">6.7.1</span></a>
      Resultat beräknas separat för varje division. Resultat i klasser tas fram från divisionsresultaten.
    </p>
    <p id="6.7.2"><a href="#6.7.2"><span class="sk">6.7.2</span></a>
      Vid resultatberäkningen skall poäng som givits enligt 6.4 i reglerna reduceras med straffpoäng enligt 6.6 i
      reglerna.
    </p>
    <p id="6.7.3"><a href="#6.7.3"><span class="sk">6.7.3</span></a>
      Om obegränsad metod eller metoden begränsade skott nyttjas så beräknas den tävlandes poängkvot genom att dela
      poängen med tiden (i sekunder). Den tävlande som har den högsta poängkvoten vinner skjutstationen, och tilldelas
      maximala stationspoäng. Stationspoängen för övriga tävlande beräknas genom att den maximala stationspoängen
      divideras med stationsvinnarens poängkvot, och produkten multipliceras med de tävlandes egna poängkvoter.
    </p>
    <p id="6.7.4"><a href="#6.7.4"><span class="sk">6.7.4</span></a>
      Om metoderna begränsad tid eller begränsad tid och skott nyttjas så vinns skjutstationen av den tävlande som
      har flest poäng, och tilldelas den maximala stationspoängen. Stationspoäng för övriga tävlande beräknas genom
      att dividera maximala stationspoängen med vinnarens poäng, varpå produkten multipliceras med de tävlandes egna
      poängsummor från skjutstationen.
    </p>
    <p id="6.7.5"><a href="#6.7.5"><span class="sk">6.7.5</span></a>
      Den minsta stationspoängen som kan ges är noll (0).
    </p>
    <p id="6.7.6"><a href="#6.7.6"><span class="sk">6.7.6</span></a>
      En tävlandes matchpoäng är summan av dennes stationspoäng.
    </p>
    <p id="6.7.7"><a href="#6.7.7"><span class="sk">6.7.7</span></a>
      Den tävlande som har högst matchpoäng är tävlingens segrare.
    </p>
    <p id="6.7.8"><a href="#6.7.8"><span class="sk">6.7.8</span></a>
      Resultat skall beräknas med ett program som godkänts av RESUL. Resultat beräknas med fyra decimaler. För att
      tydliggöra skillnaden mellan tävlande ska resultaten också presenteras som procent.
    </p>

