    <h2>1. Introduktion</h2>

    <div id="1.1">
      <h3>1.1 Generella principer</h3>
      <ul>
        <li>Sovellettu ReserviläisAmmunta (SRA) eller Tillämpat tjänsteskytte (TTS) som det benämns i Försvarsmakten,
          är en dynamisk skyttesport som riktar sig till all försvarsmaktspersonal, värnpliktiga, anställda, avtalspersonal
          samt skytteinstruktör vid FFO (Frivillig FörsvarsOrganisation). Syftet är att medge en tjänstenära idrott där
          individen kan utveckla och bibehålla sin skjutförmåga på ett säkert sätt som överensstämmer med
          försvarsmaktens instruktioner och reglementen.</li>
        <li>Den tävlande måste ha en anknytning till försvarsmakten enligt ovan. Den tävlande måste med godkänt
          resultat ha genomfört Teorikurs Grunder TTS (OBS. Gäller ej värnpliktiga under GU.) samt genomfört godkänt
          Kompetensprov BAS för de vapensystem som den tävlande avser tävla med.</li>
        <li>En tävlande som med godkänt resultat har genomfört Teorikurs Grunder TTS, kan under uppsyn och med
          tillåtelse av en Domare (RO), få delta i skjutträning, ej tävling, i syfte att öva mot Kompetensprov BAS.</li>
        <li>En tävlande som deltar i TTS utanför ordinarie tjänst (d.v.s vid föreningsverksamhet) måste vara medlem i en
          förening ansluten till Svenska Pistolskytteförbundet (SPSF).</li>
        <li>Alla tävlingar och skjutträningar inom ramen för TTS, på internationell nivå, nationell nivå, regional nivå samt
          lokal nivå, ska genomföras enligt detta regelverk. Utöver detta regelverk tillkommer tvingande styrningar från
          den centrala SRA-kommittén och SPSF-förbundsstyrelse.</li>
        <li>Den centrala SRA-kommittén och SPSF-förbundsstyrelse upprätthåller och utvecklar tekniska bilagor för
          detaljerade tolkningar och nedbrytningar av regelverket.</li>
      </ul>
    </div>

    <a id="1.2" />
    <h3>1.2 Tävlingar</h3>

    <p id="1.2.1"><a href="#1.2.1"><span class="sk">1.2.1</span></a>
      En tävling är uppdelad i ett antal skjutstationer (stages). Vid en skjutstation kan ett eller flera
      eldhandvapen brukas.
    </p>

    <p id="1.2.2"><a href="#1.2.2"><span class="sk">1.2.2</span></a>
      En tävling kan anordnas för en enskild typ av eldhandvapen eller en kombination av flera, t.ex. karbin och pistol.
    </p>

    <p id="1.2.3"><a href="#1.2.3"><span class="sk">1.2.3</span></a>
      En tävlingsinbjudan måste ange vilken eller vilka typer av eldhandvapen som kommer nyttjas i tävlingen samt
      antal skjutstationer (stages), minsta antal skott som åtgår för att genomföra tävlingen, tävlingens längsta
      skjutavstånd och andra eventuella särskilda förhållanden.
    </p>

    <p id="1.2.4"><a href="#1.2.4"><span class="sk">1.2.4</span></a>
      Internationellt finns tre divisioner inom SRA: Open, Standard samt Militär division. Den tävlandes brukade
      eldhandvapen och övrig buren utrustning styr vilken division som den tävlande får delta i.
    </p>
    <p class="se-extension">
      I Sverige finns tre divisioner inom TTS: Militär standard 5,56, Militär standard 7,62 samt Öppen (Open) division.
    </p>

    <p id="1.2.5"><a href="#1.2.5"><span class="sk">1.2.5</span></a>
      I tillägg till ovan nämnda divisioner förekommer även ett antal klasser inom divisionerna:
    </p>
    <ul>
      <li>Värnpliktsklass, nyttjas för värnpliktig personal under Grundutbildning (gäller i Sverige).</li>
      <li>Seniorklass (50+)</li>
      <li>Supersenior (60+)</li>
      <li>Damklass</li>
    </ul>
    <p>
      I respektive seniorklass ska den tävlande under tävlingsåret fylla minst 50 respektive 60 år. Vid nationella och
      internationella tävlingar ska poängräkning för dessa separata klasser genomföras om det är minst 5 anmälda
      deltagare ur respektive klass. Senior och Supersenior kan vid tävling slås ihop till en klass om deltagarantalet
      underskrider 3 deltagare per klass. Tävlingsarrangör kan dela ut särskilda pris för dessa klasser.
    </p>

    <p id="1.2.6"><a href="#1.2.6"><span class="sk">1.2.6</span></a>
      I inledningsskedet av en tävling kan tävlingsledningen hålla en gemensam genomgång för både de tävlande och
      funktionärer. Vid nationella och internationella mästerskap ska en sådan genomgång hållas. Genomgången avhandlar
      generella frågor gällande tävlingen och ska klargöra eventuella frågeställningar kopplade till ”Riktlinjer för
      tävlingar” och beskriva lokala förhållanden såsom hur personal kommer att röra sig på tävlingsområdet samt andra
      faktorer som påverkar tävlingens genomförande.
    </p>

    <p id="1.2.7"><a href="#1.2.7"><span class="sk">1.2.7</span></a>
      Tävling är slutförd när samtliga tävlande har genomfört alla skjutstationer (stager), alla protester och klagomål
      är behandlade och avgjorda samt att sluttid för resultatöversyn av sista skjutstationen har passerats.
    </p>

    <p id="1.2.8"><a href="#1.2.8"><span class="sk">1.2.8</span></a>
      Tävlingsområdet är skjutbana eller skjutfält där SRA/TTS-tävling genomförs. Om inte annat anges av
      tävlingsledningen består tävlingsområdet av skjutstationer (stager), registrering och visitationsplats,
      skjutstationernas uppehållsområde, hanteringsområde för eldhandvapen samt säkra framryckningsvägar mellan
      ovan nämnda platser.
    </p>

    <p id="1.2.9"><a href="#1.2.9"><span class="sk">1.2.9</span></a>
      Tävlingar kan vara inriktade mot ett särskilt tema där tyngdpunkten ligger på ett särskilt ämne eller fördjupning
      i syfte att ge tävlingen en särskild anda eller omväxling. Dessa ämnen eller fördjupningar får dock ej vara så
      avgränsande till sin natur att den tävlandes innehavda utrustning blir otillbörligt begränsande. Exempelvis får ej
      tävlingar vara avgränsande till en specifik modell eller tillverkare av utrustning. Om tävling anordnas med särskilt
      tema ska detta tydligt anges i tävlingsinbjudan.
    </p>

    <a id="1.3" />
    <h3>1.3 Skjutstationer (Stage)</h3>

    <p id="1.3.1"><a href="#1.3.1"><span class="sk">1.3.1</span></a>
      En skjutstation består av ett särskilt poängsatt och tidtaget skjutmoment som genomförs enligt en
      stationsbeskrivning (Stage Description). Varje tävling bör bestå av ett antal unika skjutstationer.
    </p>

    <p id="1.3.2"><a href="#1.3.2"><span class="sk">1.3.2</span></a>
      De individuella skjutstationernas resultat sammanställs till ett slutresultat som resulterar i att en
      tävlingsvinnare kan utses enligt paragraf 6.
    </p>

    <p id="1.3.3"><a href="#1.3.3"><span class="sk">1.3.3</span></a>
      En skjutstation innehåller alltid minst ett skjutmoment. Utöver detta kan skjutstationen innehålla andra
      prövningar, som t.ex. kast med blind handgranat. En skjutstation kan också innehålla förflyttningar och
      ställningsväxlingar. Moment som inte är direkta skjutmoment får inte ha en avgörande poängsättning för
      skjutstationen då den tävlandes skjutskicklighet ska vara tongivande för resultatet vid respektive skjutstation.
    </p>

    <p id="1.3.4"><a href="#1.3.4"><span class="sk">1.3.4</span></a>
      En skjutstation bör planeras så att krav på hastighet och träffsäkerhet vid genomförandet balanseras lika enligt
      principen tillräcklig verkan så snart som möjligt (TVSSSM).
    </p>

    <p id="1.3.5"><a href="#1.3.5"><span class="sk">1.3.5</span></a>
      Skjutstationer ska vara varierande, realistiska och lämpliga för skjutstationens angivna eldhandvapentyp samt ha
      relevanta övriga moment. Planeringsstyrningar för skjutstationsuppbyggnad återfinns i Riktlinjer för tävling.
    </p>

    <p id="1.3.6"><a href="#1.3.6"><span class="sk">1.3.6</span></a>
      TTS / SRA tävlingar genomförs i fri stil. Tävlingsledningen får inte styra den tävlandes val av skjutställning
      eller skjutplats vid genomförandet av en skjutstation. Dock får skjutstationen byggas upp så att den tävlande
      genom skjutstationens uppbyggnad tvingas till en specifik skjutplats eller skjutställning. Detta sker genom t.ex.
      att man begränsar den tävlandes valfrihet genom byggnation av hinder eller andra fysiska begränsningar. De
      tävlande måste få genomföra skjutstationen med valfrihet kopplat till praxis om när och hur ett specifikt mål
      kan bekämpas.
    </p>

    <p id="1.3.7"><a href="#1.3.7"><span class="sk">1.3.7</span></a>
      Ett tvingande magasinsbyte kan läggas in i en skjutstation mellan första och sista skott. Det tvingande
      magasinsbytet sker då när den tävlande så själv väljer att göra det.
    </p>

    <p id="1.3.8"><a href="#1.3.8"><span class="sk">1.3.8</span></a>
      Den tävlande får ha valfri mängd magasin buret på sin person eller på plats angiven i stationsbeskrivningen. Det
      är inte tillåtet att lägga in krav på magasinspåfyllnad under en skjutstation. Dock får en stationsbeskrivning
      ange en specifik mängd patroner i den tävlandes magasin och/eller vapen när genomförandet vid skjutstationen
      påbörjas.
    </p>

    <p id="1.3.9"><a href="#1.3.9"><span class="sk">1.3.9</span></a>
      Hela eller delar av skjutstationer får ha styrningar för om skjuthand eller stödhand ska nyttjas vid
      genomförandet.
    </p>

    <p id="1.3.10"><a href="#1.3.10"><span class="sk">1.3.10</span></a>
      En skjutstations uppbyggnad får endast ändras under pågående tävling om säkerheten kräver det. Om en
      skjutstation ändras under tävlingens gång på ett sätt som inte kan anses som försumbart så skall samtliga
      tävlande som redan hunnit genomföra skjutstationen, genomföra den igen genom en omskjutning, även kallad re-shoot.
      Om omskjutning inte är möjlig att genomföra p.g.a. yttre omständigheter eller liknande så ska den skjutstationen
      med tillhörande resultat strykas ur slutresultatet. Ett sådant beslut tas av Tävlingsledare (Match Director).
    </p>

    <p id="1.3.11"><a href="#1.3.11"><span class="sk">1.3.11</span></a>
      Minsta antal skott vid en hageltävling får inte överstiga 15 skott förutom vid särskilda tävlingar med särskilt
      tema som kräver detta.
    </p>

    <p id="1.3.12"><a href="#1.3.12"><span class="sk">1.3.12</span></a>
      Tävlande ska inte tvingas till att skjuta med enhandsvapen på papperstavla motsvarande IPSC metric där avståndet
      överstiger 25 m eller liknande reaktiva mål där avståndet överstiger 50 m. Mindre mål ska placeras hitom ovan
      nämnda avstånd.
    </p>

    <p id="1.3.13" class="se-extension"><a href="#1.3.13"><span class="sk">1.3.13</span></a>
      Vid samtliga gevärs-/karbintävlingar i Sverige skall 30 % av dukade mål vara på ett skjutavstånd överstigande 150 m.
    </p>

    <a id="1.4" />
    <h3>1.4 Skjutstationers uppbyggnad</h3>

    <p id="1.4.1"><a href="#1.4.1"><span class="sk">1.4.1</span></a>
      Arrangören kan på en skjutstation uppföra konstruktioner som leder eller begränsar skyttens rörelser och
      skjutning, exempelvis barrikader, tunnlar, begränsningslinjer och spärrlinjer.
    </p>

    <p id="1.4.2"><a href="#1.4.2"><span class="sk">1.4.2</span></a>
      En begränsningslinje används för att styra den tävlande till att röra sig inom skjutområdet. Begränsningslinjerna
      skall vara så konstruerade att de ej kan förväxlas med markeringar för pekvinklar eller skjutgränser. Den tävlande
      får ej passera en begränsningslinje under skjutstationens genomförande. Passage av sådan linje innebär procedurfel
      och bestraffas enligt 6.6.2.
    </p>

    <p id="1.4.3"><a href="#1.4.3"><span class="sk">1.4.3</span></a>
      Spärrlinjer kan nyttjas för att tvinga skytten att skjuta från en plats bakom ett fysiskt skydd. De kan placeras i
      valfri vinkel bakom ett hinder/skydd. En spärrlinje måste vara minst en (1) meter i längd, och såvida inget annat
      angivits i stationsbeskrivningen så anses de löpa oändligt i längdriktningen. Spärrlinjer nyttjas för att representera
      nyttjandet av skydd under skjutning. En spärrlinje skall vara säkert fastsatt så att dess placering inte ändras
      mellan genomföranden under tävlingen.
    </p>

    <p id="1.4.4"><a href="#1.4.4"><span class="sk">1.4.4</span></a>
      En tunnel kan vara ett heltäckande rör eller en Tiitinen- (Cooper-) tunnel vars tak består av ej fastsatta
      käppar/pinnar. En Tiitinen-tunnel representerar en situation i vilken skytten behöver röra sig i låga ställningar.
    </p>

    <p id="1.4.5"><a href="#1.4.5"><span class="sk">1.4.5</span></a>
      I de fall det inte är möjligt att bygga skjutstationskonstruktioner med anledning av skjutbanebestämmelser,
      begränsad tid eller brist på byggmaterial m.m. så är det tillåtet att endast använda spärrlinjer och
      begränsningslinjer för att hindra beskjutning av mål från platser utanför förbjudet område. Denna regel gäller
      endast lokala tävlingar eller tävlingar på lägre nivå. Ytterligare information om krav på skjutstationskonstruktioner
      och rekommenderade konstruktioner finns i dokumentet ”Riktlinjer för tävlingar”.
    </p>

    <a id="1.5" />
    <h3>1.5 Tidtagning</h3>

    <p id="1.5.1"><a href="#1.5.1"><span class="sk">1.5.1</span></a>
      Tidtagning utförs med kommersiella tidtagare (skjuttimer) eller andra lämpliga och pålitliga instrument.
      Tidtagaren måste avge en startsignal och registrera skjutna skott och tid. Det är inte nödvändigt att varje skott
      registreras utom det sista, såvida det inte krävs för poänggivningen, exempelvis vid ”begränsade skott”.
    </p>

    <p id="1.5.2"><a href="#1.5.2"><span class="sk">1.5.2</span></a>
      Efter att det sista skottet har avlossats skall domaren säkerställa att ljud som avges vid patron ur på vapnet,
      såsom mantelrörelse eller blindavfyring, inte registreras som ett skott i tidtagarinstrumentet.
    </p>

    <p id="1.5.3"><a href="#1.5.3"><span class="sk">1.5.3</span></a>
      Om en signaturdämpare förhindrar att skotten registreras av tidtagaren så skall domaren fastställa
      tillvägagångssätt för att det sista skottet skall registreras. Provskjutning enligt principen 4.1.10 må genomföras.
      Exempelvis kan domaren knacka på mikrofonen på skjuttimern för att tiden skall registreras.
    </p>

