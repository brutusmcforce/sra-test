    <a id="5" />
    <h2>5. Eldhandvapen och Utrustning</h2>

    <a id="5.1" />
    <h3>5.1 Allmänt</h3>
    <p id="5.1.1"><a href="#5.1.1"><span class="sk">5.1.1</span></a>
      Den tävlande deltar i den division som denne registrerats i inför tävlingen. Den tävlandes eldhandvapen och
      utrustning skall uppfylla kraven för aktuell division. Vapenkontroll skall som minst genomföras vid nationellt
      mästerskap eller högre nivå, och skall omfatta eldhandvapnets säkerhet och överensstämmelse med bestämmelserna
      för avsedd division.
    </p>
    <p class="se-extension">
      <strong>Militära bestämmelser:</strong>
      Vid Försvarsmaktsmästerskap bör behörig vapentekniker genomföra vapenkontroll på tjänstevapen (FM-klass 5,56 /
      7,62). Kontrollen skall omfatta säkerhetskontroll (SK), och kontroll att grundtillsyn (GT) har genomförts inom
      godkänd period.
    </p>
    <p id="5.1.2"><a href="#5.1.2"><span class="sk">5.1.2</span></a>
      Alla eldhandvapen och all buren utrustning som nyttjas under tävlingen skall vara säkra att använda. Detta är
      den tävlandes ansvar att tillse.
    </p>
    <p id="5.1.3"><a href="#5.1.3"><span class="sk">5.1.3</span></a>
      Förändring eller modifiering som minskar ett vapens säkerhet är förbjudet. Det är inte tillåtet att åsidosätta
      en säkring, detta gäller dock inte grepp- och avtryckarsäkring som är tillåtna såvida vapnet är försett med en
      annan säkring som kan nyttjas av skytten.
    </p>
    <p id="5.1.4"><a href="#5.1.4"><span class="sk">5.1.4</span></a>
      Den tävlandes eldhandvapen eller division kan inte ändras under tävlingen utan tillstånd från tävlingsledaren
      (MD) eller banchefen (RM). Vapenklass kan endast ändras från Standarddivisioner eller Militär division till
      Öppen division. Att byta eldhandvapen eller dess huvuddelar kräver tillstånd från tävlingsledaren (MD) eller
      banchefen (RM). Annan utrustning kan bytas efter behov. Till ett vapens huvuddelar räknas sikte, pipa, låda,
      underbeslag och ramstycke, mekanism, slutstycke eller motsvarande del såvida den inte innefattar slutstyckshuvudet
      vilket är tillåtet att byta utan tillstånd. En förstoringssats är inte att betrakta som en huvuddel. Inför en
      skarpskyttestation är det, trots denna bestämmelse, tillåtet att byta sikte utan särskilt tillstånd.
    </p>
    <p class="se-extension">
      <strong>Militära bestämmelser:</strong>
      Det är inte tillåtet att byta ett slutstyckshuvud eller motsvarande del i Försvarsmaktens vapen. Detta får
      endast göras av behörig vapentekniker.
    </p>
    <p id="5.1.5"><a href="#5.1.5"><span class="sk">5.1.5</span></a>
      Om en tävlandes eldhandvapen eller burna utrustning inte uppfyller kraven i någon division så registreras inget
      resultat för skytten.
    </p>
    <p id="5.1.6"><a href="#5.1.6"><span class="sk">5.1.6</span></a>
      Kontroll av en tävlandes eldhandvapen eller burna utrustning får göras av tävlingsledare (MD), banchef (RM) eller
      domare (RO) när som helst under en tävling.
    </p>
    <p id="5.1.7"><a href="#5.1.7"><span class="sk">5.1.7</span></a>
      Om ett eldhandvapen eller en buren utrustningsdetalj inte bedöms som säker så måste den/det repareras eller
      bytas för att den tävlande ska få fortsätta tävlingen.
    </p>
    <p id="5.1.8"><a href="#5.1.8"><span class="sk">5.1.8</span></a>
      Ett eldhandvapen bedöms inte som säkert om säkring, säkerhetsupphak (hanuppfång), hanspärr, avspänningsregel
      eller tändstiftssäkring inte fungerar, eller om det skjuter eldskurar eller helautomatisk eld då
      säkringsomställaren (motsv.) är ställd på patronvis eld. Ett eldhandvapen får inte avlossa skott då avtryckaren
      släpps fram eller om det tappas på ett hårt underlag.
    </p>
    <p id="5.1.9"><a href="#5.1.9"><span class="sk">5.1.9</span></a>
      Banchefen (RM) beslutar huruvida ett eldhandvapen eller buren utrustning uppfyller kraven i reglerna.
    </p>
    <p id="5.1.10"><a href="#5.1.10"><span class="sk">5.1.10</span></a>
      Banchefen (RM) äger rätt att förbjuda ett särskilt eldhandvapen eller en särskild typ av ammunition vid en
      tävling, eller för vissa stationer, med anledning av:
    </p>
    <ul>
      <li>tävlingsregler, skjutbanebestämmelser (motsv.) eller</li>
      <li>om det annars finns risk att skada utrustning på skjutbanan</li>
      <li>säkerhetsskäl.</li>
    </ul>
    <p id="5.1.11"><a href="#5.1.11"><span class="sk">5.1.11</span></a>
      Alla patroner skall ha en kraftfaktor om minst 2450 [kulvikt i gram (g) × utgångshastighet i meter per sekund (m/s)].
    </p>
    <p>
      En exakt mätning av kraftfaktorn krävs vanligen inte. Emellertid så kan en mätning genomföras då det finns
      rimligt tvivel att kraftfaktorn inte uppfylls. Mätningen genomförs genom att väga kulan från en (1) patron och
      mäta utgångshastigheten från fyra (4) patroner, varvid medelvärdet av utgångshastigheterna nyttjas.
    </p>
    <p id="5.1.12"><a href="#5.1.12"><span class="sk">5.1.12</span></a>
      Eldhandvapen som inte används för att avlossa skott vid en station kan definieras som ’burna men ej nyttjade’.
      Domaren (RO) skall visitera eldhandvapnet före stationen påbörjas. Patronlägesflagga, kontrolldon eller
      motsvarande bör om möjligt monteras.
    </p>

    <a id="5.2" />
    <h3>5.2 Eldhandvapen</h3>
    <p id="5.2.1"><a href="#5.2.1"><span class="sk">5.2.1</span></a>
      Vid en tävling kan följande eldhandvapen nyttjas:
    </p>
    <ul>
      <li>Karbin</li>
      <li>Gevär</li>
      <li>Pistol</li>
      <li>Revolver</li>
      <li>Skarpskyttegevär</li>
      <li>Prickskyttegevär</li>
      <li>Hagelgevär</li>
    </ul>
    <p>Närmare bestämmelser anges i bilaga 2–5.</p>

    <a id="5.3" />
    <h3>5.3 Utrustning</h3>
    <p id="5.3.1"><a href="#5.3.1"><span class="sk">5.3.1</span></a>
      Bärande av ett eldhandvapen eller magasin regleras inte, men det ska vara säkert och robust. Vid skjutstationens
      början bär den tävlande magasinen och ammunitionen i sin burna utrustning om inte stationsbestämmelserna anger
      annat.
    </p>
    <p id="5.3.2"><a href="#5.3.2"><span class="sk">5.3.2</span></a>
      Till en pistol skall den tävlande ha ett hölster av tjänstetyp.
    </p>
    <p id="5.3.2.1"><a href="#5.3.2.1"><span class="sk">5.3.2.1</span></a>
      Hölster som medger att vapnet rör sig inuti hölstret på ett sätt så att vapnet kan osäkras eller avfyras (i
      hölstret) är förbjudna. Vidare är hölster vars låsmekanism avaktiveras med ett finger som riskerar att (i draget)
      oavsiktligen hamna innanför varbygeln förbjudna.
    </p>
    <p id="5.3.2.2"><a href="#5.3.2.2"><span class="sk">5.3.2.2</span></a>
      Ett hölster ska skydda vapnet från smuts och skada. Det ska som minst täcka kornet, mantelns överdel och sidor
      från mynningen till främre delen av utkastaröppningen och avtryckaren (varbygeln). En mantel med öppning över
      pipan skall täckas minst halva dess längd, och en revolvers hela pipa skall vara täckt.
    </p>
    <p id="5.3.2.3"><a href="#5.3.2.3"><span class="sk">5.3.2.3</span></a>
      Hölster skall ha en pålitlig låsmekanism som förhindrar att vapnet kan tappas. Den tävlande skall, på anmodan av
      en tävlingsfunktionär, bevisa att låsningen är säker. Hölstret testas av en domare (RO) på stationsområdet.
      Förfarande:
    </p>
    <ol>
      <li>Ett fullt magasin sätts i den i övrigt oladdade pistolen. Revolver testas utan ammunition.</li>
      <li>Det löstagna hölstret vänds uppochned med mynningen pekandes inom pekvinklarna.</li>
      <li>Om vapnet lossnar från hölstret är hölstret förbjudet att använda vid tävlingen till dess att det reparerats
        och klarar testet.</li>
    </ol>
    <p id="5.3.2.4"><a href="#5.3.2.4"><span class="sk">5.3.2.4</span></a>
      Hölstrets konstruktion och placering måste medge att den tävlande kan dra vapnet med mynningen i ofarlig
      riktning och utan att svepa sig själv. När vapnet är hölstrat och skytten står i grundställning skall mynningen
      inte peka mer än två meter bort från den tävlande (räknat där kärnlinjen träffar marken).
    </p>
    <p id="5.3.3"><a href="#5.3.3"><span class="sk">5.3.3</span></a>
      De tävlandes och domarnas (RO) klädsel skall vara tålig nog för fältbruk och ur allmän synpunkt anständig.
      Domare (RO) ska vara tydligt utmärkt t.ex. med varselväst.
    </p>
    <p class="se-extension">
      <strong>Militära bestämmelser:</strong>
      Tävlande i FM-klass (5,56 och 7,62) skall bära reglementsenlig uniform. Fältlystran anses råda på
      tävlingsområdet varför lättnader och anpassning av klädsel är tillåten, utom vid prisutdelningar eller liknande
      ceremonier. Det är aldrig tillåtet att till uniform nyttja märken och tecken som inte utgör tjänstetecken, t.ex.
      ”patchar” med företagslogotyper (motsv.).
    </p>
    <p id="5.3.4"><a href="#5.3.4"><span class="sk">5.3.4</span></a>
      Militärdivisionens närmare bestämmelser framgår i bilaga 6.
    </p>

