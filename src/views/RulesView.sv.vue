    <a id="7" />
    <h2>7. Poängräkning</h2>
    <p id="7.1"><a href="#7.1"><span class="sk">7.1</span></a>
      Varje tävlande är ansvarig för att föra sin egen poängräkning vid sidan av protokollföraren (SK) och att
      kontrollera att dessa överensstämmer. Poängräkning kan ske elektroniskt eller på pappersblankett.
    </p>
    <p id="7.2"><a href="#7.2"><span class="sk">7.2</span></a>
      Domaren (RO) är ansvarig för att kontrollera att nedtecknad information är korrekt innan denne skriver under.
      Därefter skriver den tävlande under. Efter att båda har signerat protokollet får inga ändringar göras i
      protokollet utom för att korrigera eventuell felberäkning av totalsumman för skjutstationen.
    </p>
    <p id="7.3"><a href="#7.3"><span class="sk">7.3</span></a>
      Om domaren (RO) och den tävlande är oense om poängräkningen ska detta noteras i protokollet och därefter träder
      regel 8.2 i kraft.
    </p>
    <p id="7.4"><a href="#7.4"><span class="sk">7.4</span></a>
      Om det föreligger ett misstag i det officiella resultatet för en skjutstation ska den tävlande som drabbats
      begära korrigering inom i för tävlingen i tävlingsgenomgången annonserad verifikationstid. Funktionärer ska då
      genomföra kontrollräkning med eventuell korrigering. Om så inte sker träder regel 8.1.2 i kraft.
    </p>
    <p id="7.5"><a href="#7.5"><span class="sk">7.5</span></a>
      Det officiella tävlingsresultatet får inte publiceras innan skjutstationsresultaten är publicerade och samtliga
      tävlande har haft möjlighet att verifiera sina resultat.
    </p>

