    <a id="2" />
    <h2>2. Tävlingsledning</h2>

    <a id="2.1" />
    <h3>2.1 Tävlingskommissarie (Supervisor)</h3>
    <p id="2.1.1"><a href="#2.1.1"><span class="sk">2.1.1</span></a>
      En tävlingskommissarie som är godkänd av ansvarig organisation skall utses för nationella och internationella
      mästerskap. Tävlingskommissarien skall vara förordnad instruktör eller överdomare inom TTS/SRA.
    </p>

    <a id="2.2" />
    <h3>2.2 Tävlingsledare (Match Director)</h3>
    <p id="2.2.1"><a href="#2.2.1"><span class="sk">2.2.1</span></a>
      En tävlingsledare (MD) måste vara förordnad som TTS-domare.
    </p>
    <p id="2.2.2"><a href="#2.2.2"><span class="sk">2.2.2</span></a>
      Tävlingsledaren (MD) är övergripande ansvarig för tävlingen och ansvarar för dess administration, tidtagning,
      skjutstationsmaterial samt mat, sjukvård, övriga tjänster och säkerheten.
    </p>
    <p id="2.2.3"><a href="#2.2.3"><span class="sk">2.2.3</span></a>
      Tävlingsledaren (MD) är den yttersta auktoriteten i alla uppräknade avseenden med undantag för protesthantering.
    </p>
    <p id="2.2.4"><a href="#2.2.4"><span class="sk">2.2.4</span></a>
      Tävlingsledaren (MD) är ordförande i tävlingsjuryn (arbitration committee) utan egen rösträtt.
    </p>
    <p id="2.2.5"><a href="#2.2.5"><span class="sk">2.2.5</span></a>
      Vid klubbtävlingar och lokala tävlingar kan tävlingsledaren (MD) även fullgöra andra roller, t.ex. banchef (RM) och
      domare (RO).
    </p>

    <a id="2.3" />
    <h3>2.3 Banchef (Range Master)</h3>
    <p id="2.3.1"><a href="#2.3.1"><span class="sk">2.3.1</span></a>
      Banchefen (RM) skall vara en förordnad TTS-domare. Banchef (RM) utses av tävlingsledaren (MD).
    </p>
    <p id="2.3.2"><a href="#2.3.2"><span class="sk">2.3.2</span></a>
      Banchefens (RM) åligganden:
    </p>
    <ol type="a">
      <li>Ansvarar för skjutstationernas säkerhet och deras säkerhetsmässiga planering.</li>
      <li>Övervakar att alla regler och bestämmelser åtföljs och ansvarar för att utbilda och handleda
        skjutstationernas funktionärer.</li>
      <li>Alla diskvalificeringar, protester och säkerhetsförseelser skall meddelas banchefen (RM).</li>
      <li>Arbetsleder skjutstationsfunktionärer under tävlingen.</li>
      <li>Ansvarar för att alla tävlingsregler följs och tillämpas lika för alla tävlande.</li>
    </ol>
    <p id="2.3.3"><a href="#2.3.3"><span class="sk">2.3.3</span></a>
      Vid klubbtävlingar och lokala tävlingar så kan banchefen (RM) även fullgöra andra roller, t.ex. domare (RO).
    </p>

    <a id="2.4" />
    <h3>2.4 Tävlingssekreterare (Statisticals Officer)</h3>
    <p id="2.4.1"><a href="#2.4.1"><span class="sk">2.4.1</span></a>
      Tävlingssekreteraren (SO) är den funktionär som ansvarar för att samla in, sortera och förvara
      tävlingsprotokoll såväl samt producera, verifiera och publicera preliminära och slutliga tävlingsresultat och
      statistik.
    </p>
    <p id="2.4.2"><a href="#2.4.2"><span class="sk">2.4.2</span></a>
      Denne säkerställer att alla ofullständiga eller felaktiga protokollföringar omedelbart verifieras hos banchefen (RM).
      Tävlingssekreteraren (SO) är direkt underställd banchefen (RM).
    </p>
    <p id="2.4.3"><a href="#2.4.3"><span class="sk">2.4.3</span></a>
      Tävlingssekreteraren (SO) vid nationella och internationella mästerskap skall vara en förordnad TTS-domare (RO).
    </p>

    <a id="2.5" />
    <h3>2.5 Domare (Range Officer)</h3>
    <p id="2.5.1"><a href="#2.5.1"><span class="sk">2.5.1</span></a>
      En domare (RO) vid en TTS-tävling skall ha genomfört en godkänd utbildning till domare för TTS/SRA och inneha
      förordnande som sådan.
    </p>
    <p id="2.5.2"><a href="#2.5.2"><span class="sk">2.5.2</span></a>
      Domarens (RO) åligganden:
    </p>
    <ol type="a">
      <li>Ansvarar för skjutstationens säkerhet.</li>
      <li>Håller stationsgenomgång.</li>
      <li>Övervakar den tävlandes åtlydnad av stationsgenomgången och bevakar den tävlandes säkerhetsmässiga
        uppträdande.</li>
      <li>Ger kommandon på skjutstation.</li>
      <li>Kungör tid och poäng, och tillser att dessa protokollförs. Domaren (RO) kan biträdas av en
        protokollförare (SK).</li>
      <li>Bevakar skjutstationsdukningens och målens skick.</li>
      <li>Ansvarar för uppbyggnad och allmän ordning vid sin skjutstation och dess uppehållsområde och
        hanteringsområde.</li>
    </ol>
    <p id="2.5.3"><a href="#2.5.3"><span class="sk">2.5.3</span></a>
      Domaren (RO) har beslutsrätt över all personal vid sin skjutstation.
    </p>
    <p id="2.5.4"><a href="#2.5.4"><span class="sk">2.5.4</span></a>
      Domaren (RO) är en tävlingsfunktionär som är förordnad som TTS-domare. Då en skjutstation har flera domare (RO),
      ska en utses till att föra befälet och ansvara för skjutstationen.
    </p>

    <a id="2.6" />
    <h3>2.6 Protokollförare (Score Keeper)</h3>
    <p id="2.6.1"><a href="#2.6.1"><span class="sk">2.6.1</span></a>
      Protokollföraren (SK) ska ha kompetens att nyttja poängräkningssystemet. Protokollförarens åligganden kan
      kombineras med andra roller förutom vid nationella och internationella mästerskap.
    </p>
    <p id="2.6.2"><a href="#2.6.2"><span class="sk">2.6.2</span></a>
      Protokollförarens (SK) åligganden:
    </p>
    <ol type="a">
      <li>Fastställer skjutordningen, om så krävs.</li>
      <li>Hanterar poängräkningssystemet för skjutstationen.</li>
      <li>Dokumenterar tydligt skjutstationens resultat i poängräkningssystemet och säkerställer att protokollen
        signeras av domaren (RO) och den tävlande.</li>
      <li>Kungör nästa tävlande och tävlande därnäst.</li>
    </ol>

    <a id="2.7" />
    <h3>2.7 Övrig tävlingsorganisation</h3>
    <p id="2.7.1"><a href="#2.7.1"><span class="sk">2.7.1</span></a>
      I Riktlinjer för tävlingar listas ytterligare information om tävlingsfunktionärer som kan behövas för att
      arrangera större tävlingar såsom nationella mästerskap. Det dokumentet anger i vissa fall funktionärers
      åligganden ytterligare i detalj.
    </p>

    <hr />

    <a id="3" />
    <h2>3. Säkerhetsregler</h2>

    <a id="3.1" />
    <h3>3.1 Generella säkerhetsregler gällande eldhandvapen</h3>
    <p>De grundläggande säkerhetsreglerna är:</p>
    <ul>
      <li>Hantera alltid vapen som om de är laddade!</li>
      <li>Rikta inte mynningen mot något som du inte är beredd att bekämpa!</li>
      <li>Håll fingret från avtryckaren tills vapnet riktas mot målet!</li>
      <li>Var säker på ditt mål och dess omgivning!</li>
    </ul>

    <a id="3.2" />
    <h3>3.2 Övergripande säkerhet</h3>
    <p id="3.2.1"><a href="#3.2.1"><span class="sk">3.2.1</span></a>
      Tävlingsledaren ska tillsammans med banchefen (RM) tillse att samtliga stationsbeskrivningar samt skjutstationer
      är utformade på ett sådant sätt att de tävlandes säkerhet inte äventyras. Likaså skall tävlingsledare (MD) och
      banchef (RO) tillse att stationsbeskrivningarna inte tvingar att de tävlande bryter mot säkerhetsreglerna för
      att kunna genomföra skjutstationen.
    </p>
    <p id="3.2.2"><a href="#3.2.2"><span class="sk">3.2.2</span></a>
      Tydligt utmärkta och definierade säkra uppehållsplatser för tävlande, besökare och funktionärer ska finnas.
    </p>
    <p id="3.2.3"><a href="#3.2.3"><span class="sk">3.2.3</span></a>
      En tävlande kan av en domare (RO) diskvalificeras från fortsatt deltagande i tävlingen på grund av den tävlandes
      bristande säkerhet. Domaren (RO) ska vid ett sådant tillfälle snarast meddela banchefen (RM) som säkerställer att
      diskvalificeringsgrunden är rätt och enligt reglerna och därefter verifierar om den tävlande ska diskvalificeras
      eller ej.
    </p>
    <p id="3.2.4"><a href="#3.2.4"><span class="sk">3.2.4</span></a>
      Tävlingsledaren (MD) kan avvisa och diskvalificera en tävlande från pågående tävling på grund av bristande
      sportmannaskap eller brott mot gällande säkerhetsregler.
    </p>
    <p id="3.2.5"><a href="#3.2.5"><span class="sk">3.2.5</span></a>
      Samtliga närvarande vid en tävling ska använda hörselskydd och skyddsglasögon under all pågående skjutning.
      En domare (RO) ska tillse att all personal vid sin skjutstation har applicerat hörselskydd och skyddsglasögon
      innan denne påbörjar skjutning vid sin skjutstation. Om domaren (RO) upptäcker att den tävlandes hörselskydd
      och/eller skyddsglasögon inte är korrekt applicerade under pågående skjutning ska skjutningen omedelbart avbrytas
      och skjutstationen ska efter att ovanstående är åtgärdat, skjutas om (re-shoot). Om den tävlande oavsiktligt tappar
      hörselskydd och/eller skyddsglasögon skall den tävlande omedelbart åtgärda detta eller avbryta skjutstationen med
      vapnet i skjutriktningen och meddela domaren (RO) om vad som har inträffat. Om så har skett, ska skjutstationen
      skjutas om. Om den tävlande genom medveten handling lösgör burna hörselskydd och/eller skyddsglasögon i syfte att
      få till en omskjutning så diskvalificeras den tävlande på grund av bristande sportmannaskap.
    </p>

    <a id="3.3" />
    <h3>3.3 Osäker hantering av eldhandvapen</h3>
    <p><em>Överträdelser mot nedan följande säkerhetsregler leder till omedelbar diskvalificering från tävling.</em></p>

    <h4>Tävlingsområde (Match area)</h4>

    <p id="3.3.1"><a href="#3.3.1"><span class="sk">3.3.1</span></a>
      När ett eldhandvapen befinner sig utanför en skjutstation ska vapnet vara oladdat, tomt på ammunition och slackat.
      Om möjligt ska vapnet också vara säkrat. Användning av ett monterat kontrolldon, kammarflagga eller säkerhetspropp
      är tillåtet.
    </p>
    <p id="3.3.2"><a href="#3.3.2"><span class="sk">3.3.2</span></a>
      Ett eldhandvapen är laddat om:
    </p>
    <ul>
      <li>Vapnet har en patron i patronläget.</li>
      <li>Mekanismen är i öppet/tillbakafört läge och ett magasin innehållande ammunition är applicerat i
        magasinsstyrningen.</li>
    </ul>
    <p id="3.3.3"><a href="#3.3.3"><span class="sk">3.3.3</span></a>
      Utanför skjutstation ska enhandsvapen endast transporteras antingen buret i ett hölster eller i en för ändamålet
      lämplig låda eller fodral. Tvåhandsvapen såsom gevär, karbin, hagelvapen transporteras i vapenrem med pipan
      antingen pekandes uppåt alternativt neråt om inte vapnet transporteras i för ändamålet lämplig låda eller fodral.
      Skjutfingret ska under all tid utanför skjutstation vara från avtryckaren.
    </p>
    <p id="3.3.4"><a href="#3.3.4"><span class="sk">3.3.4</span></a>
      Om ett eldhandvapen av någon anledning tappas utanför skjutstation får endast domare (RO) plocka upp vapnet.
      När så skett transporterar domaren (RO) vapnet till skjutstation, ej hanteringsområdet, där vapnet kontrolleras
      att det uppfyller kriterierna under punkten 3.3.1. Den tävlande kommer ej att diskvalificeras under förutsättning
      att inga överträdelser mot säkerheten skett.
    </p>
    <p id="3.3.5"><a href="#3.3.5"><span class="sk">3.3.5</span></a>
      Utanför skjutstation får ett eldhandvapen endast hanteras i ett särskilt definierat och utmärkt hanteringsområde
      (Safety Area). Hanteringsområdet ska ha utmärkta pekvinklar inom vilka vapnets pipa alltid skall riktas vid
      hantering (ofarlig riktning). Följande är ansett att hantera ett eldhandvapen: ihop-/isärtagning, samtliga
      eldhandgrepp, öva drag, inta skjutställningar/greppa pistolgreppet.
    </p>
    <p id="3.3.6"><a href="#3.3.6"><span class="sk">3.3.6</span></a>
      Hanteringsområdet (Safety Area) måste placeras vid lämplig plats nära skjutstation med tydligt märkta
      instruktioner. Hanteringsområdets gränser och vinklar ska vara tydligt definierade och utmärkta. Den tävlande
      nyttjar hanteringsområdet för följande:
    </p>
    <ul>
      <li>Packning och uppackning samt hölstring av oladdade eldhandvapen.</li>
      <li>Torrövning med blindavfyrning samt för att öva drag med oladdade eldhandvapen.</li>
      <li>Torrövning av magasinsbyte och omladdning med tomma magasin.</li>
      <li>Visitation, vård och reparation av eldhandvapen och tillbehör.</li>
    </ul>
    <p>
      Vid all vapenhantering i ett hanteringsområde ska vapnets pipa riktas innanför utmärkta pekvinklar. Blind
      ammunition, fyllda magasin, snabbladdare, ammunitionslådor eller lös ammunition får under inga omständigheter
      hanteras i hanteringsområdet. Ej heller får någon ammunition där fästas på eller i vapnet.
    </p>
    <p id="3.3.7"><a href="#3.3.7"><span class="sk">3.3.7</span></a>
      En registrerings- och visitationsplats för eldhandvapen (Firearm compliance Inspection Area) upprättas av
      tävlingsledningen. Vid en registrerings- och visitationsplats för eldhandvapen får endast funktionärer hantera
      eldhandvapen om inte andra särskilda styrningar för detta har givits av tävlingsledningen. Vid denna plats gäller
      samma regler som under punkten 3.3.6.
    </p>

    <h4>Skjutstation / stage area (Stage)</h4>

    <p id="3.3.8"><a href="#3.3.8"><span class="sk">3.3.8</span></a>
      Ett eldhandvapen får endast laddas och hanteras efter instruktioner från skjutstationens domare (RO).
    </p>
    <p id="3.3.8.1"><a href="#3.3.8.1"><span class="sk">3.3.8.1</span></a>
      En tävlande blir inte diskvalificerad om denne vid kommando ”magasin i” (lipas kiinnitä) också laddar vapnet
      genom att göra mekanismrörelse. Om så sker ger domaren (RO) kommando ”patron ur, blindavfyra”. Efter så skett
      intar den tävlande till startposition och inväntar domarens kommandon för fortsatt verksamhet.
    </p>
    <p id="3.3.8.2"><a href="#3.3.8.2"><span class="sk">3.3.8.2</span></a>
      En tävlande blir ej diskvalificerad om denne startar sitt genomförande på grund av felaktig given startsignal.
    </p>
    <p id="3.3.9"><a href="#3.3.9"><span class="sk">3.3.9</span></a>
      En laddad pistol eller revolver får aldrig via stationsbeskrivning (stage description) tvingas till hölstring
      under genomförande av en skjutstation. Om den tävlande på eget bevåg väljer att hölstra en laddad pistol eller
      revolver under ett genomförande får detta endast ske om vapnet är säkrat. En pistol eller revolver som saknar
      yttre säkring får endast hölstras om mekanismen är slackad och/eller loppet är tomt. Pistol med avtryckarsäkring
      som t.ex. Glock räknas som säkrad när fingret är utanför varbygeln.
    </p>
    <p id="3.3.10"><a href="#3.3.10"><span class="sk">3.3.10</span></a>
      Under skjutmomentets genomförande ska den tävlandes eldhandvapen alltid vara riktad med pipan innanför
      skjutstationens pekvinklar. En pistol eller revolvers pipa ska alltid vara riktad innanför pekvinklarna vid
      hölstring, drag och buren laddad i hölster (detta gäller oavsett startposition) alternativt vara riktad mot en
      punkt maximalt två meter framför den tävlande i skjutriktningen.
    </p>
    <p id="3.3.11"><a href="#3.3.11"><span class="sk">3.3.11</span></a>
      Ett eldhandvapens mynning får aldrig vara riktat mot den tävlande själv. Detta gäller ej vid hölstrat vapen,
      under hölstring eller drag när vapnets pipa befinner sig i hölstret och den tävlandes hantering av ett riktat
      vapen mot sig själv kan anses som försumbar.
    </p>
    <p id="3.3.12"><a href="#3.3.12"><span class="sk">3.3.12</span></a>
      Fingret ska vara utanför varbygel vid drag, hölstring, magasinsbyte, omladdning och vid åtgärdande av
      eldavbrott. Om fingret måste vara innanför varbygeln vid åtgärdande av eldavbrott ska detta kommuniceras till
      domare (RO) som då beviljar eller avslår innan åtgärden vidtas.
    </p>
    <p id="3.3.13"><a href="#3.3.13"><span class="sk">3.3.13</span></a>
      Skjutfingret ska vara utanför varbygeln om den tävlande tar mer än ett steg och/eller byter skjutställning om
      detta inte sker i samband med skjutning eller siktande mot för den tävlande synliga mål. Att kontrollerat rikta
      ett eldhandvapen mot för den tävlande synliga mål är att betrakta som att sikta.
    </p>
    <p id="3.3.14"><a href="#3.3.14"><span class="sk">3.3.14</span></a>
      Den tävlande får endast släppa fysisk kontakt med sitt eldhandvapen enligt beskrivning i stationsbeskrivningen
      eller om situationen så kräver och då endast när vapnet är säkrat och det placeras på marken eller annat stadigt
      underlag på ett kontrollerat sätt. Om så skett får den tävlande ej röra sig längre bort än en meter ifrån
      eldhandvapnet. Om den tävlande släpper fysisk kontakt med sitt eldhandvapen på annat sätt än ovan beskrivet,
      avbryter domaren (RO) genomförandet och genomför patron ur på eldhandvapnet. Om den tävlandes fysiska kontakt
      med ett för skjutstationen buret men ej nyttjat eldhandvapen sker på ett kontrollerat sätt ska detta inte vara
      grund för av domaren (RO) avbrutet genomförande och/eller diskvalifikation.
    </p>
    <p id="3.3.15"><a href="#3.3.15"><span class="sk">3.3.15</span></a>
      En stationsbeskrivning får innehållande tvingande regel att gevär, karbin och hagelvapen ska säkras vid all
      förflyttning och ställningsväxling om säkerheten på platsen så kräver.
    </p>
    <p id="3.3.16"><a href="#3.3.16"><span class="sk">3.3.16</span></a>
      Automateld är inte tillåtet om inte annat specifikt anges aktuell stationsbeskrivning.
    </p>
    <p id="3.3.17"><a href="#3.3.17"><span class="sk">3.3.17</span></a>
      Om den tävlande skjuter vådaskott ska domaren (RO) omedelbart avbryta genomförandet och den tävlande är
      automatiskt diskvalificerad från tävlingen.
    </p>
    <p id="3.3.18"><a href="#3.3.18"><span class="sk">3.3.18</span></a>
      Inom TTS/SRA utgörs ett vådaskott av följande:
    </p>
    <ol type="a">
      <li>Alla skott som avfyras av den tävlande innan startsignal eller efter att ”Patron ur, spänn upp för
        visitation” (liikkuvat taakse) är kommenderat.</li>
      <li>Alla skott som avfyras utanför angivna skjutgränser.</li>
      <li>Alla skott som träffar marken närmare än 3 meter från den tävlande om inte annat anges i
        stationsbeskrivningen.</li>
      <li>Alla skjutna skott som uppenbart inte var avfyrade avsiktligt.</li>
    </ol>
    <p id="3.3.19"><a href="#3.3.19"><span class="sk">3.3.19</span></a>
      Ett vådaskott som avfyras innanför skjutgränserna leder inte automatiskt till diskvalificering av den tävlande
      om vådaskottet kan härledas till ett verifierbart tekniskt problem med vapnet. Om så sker så tilldelas den
      tävlande noll poäng för aktuell skjutstation och den tävlande ska innan fortsatt tävlande, utan dröjsmål
      reparera sitt vapen eller avfärda det från aktuell tävling om så ej kan ske. Ett sådant vådaskott ske genast
      rapporteras till banchefen (RM). Vid upprepat vådaskott under samma förutsättningar av samma tävlande ska denne
      diskvalificeras från tävlingen.
    </p>
    <p id="3.3.20"><a href="#3.3.20"><span class="sk">3.3.20</span></a>
      Om den tävlande nyttjar en för tävlingen otillåten ammunitionstyp, räknas detta som en överträdelse mot gällande
      säkerhetsregler och den tävlande diskvalificeras från tävlingen.
    </p>

    <a id="3.4" />
    <h3>3.4 Pekvinklar och skjutgränser (Sectors)</h3>
    <p id="3.4.1"><a href="#3.4.1"><span class="sk">3.4.1</span></a>
      Alla skjutstationer ska ha tydligt utmärkta pekvinklar och skjutgränser. Dessa ska vara tydligt markerade så att
      både den tävlande och domaren (RO) kan försäkra sig om att skjutstationen kan genomföras på ett säkert sätt.
      Dessa pekvinklar och skjutgränser berör inte vapen som är att betrakta som för skjutstationen burna men ej
      nyttjade.
    </p>
    <p id="3.4.2"><a href="#3.4.2"><span class="sk">3.4.2</span></a>
      Pekvinklarna utgör den begränsning inom vilken ett eldhandvapens mynning alltid ska peka inom vid genomförandet
      av en skjutstation.
    </p>
    <p id="3.4.3"><a href="#3.4.3"><span class="sk">3.4.3</span></a>
      I vertikal riktning är pekvinkeln 180 grader om inte den fysiska miljön hindrar detta.
    </p>
    <p id="3.4.4"><a href="#3.4.4"><span class="sk">3.4.4</span></a>
      I horisontal riktning är pekvinklarna 180 grader om inte annat fastställs i stationsbeskrivningen.
    </p>
    <p id="3.4.5"><a href="#3.4.5"><span class="sk">3.4.5</span></a>
      Skjutgränserna är det definierade området inom vilket alla skott ska avfyras vid en skjutstation. Skjutgränserna
      är alltid innanför pekvinklarna.
    </p>
    <p id="3.4.6"><a href="#3.4.6"><span class="sk">3.4.6</span></a>
      Skjutgränsen i höjd är normalt sett nedanför toppen på målvallen/målväggen om inte annat är beskrivet i
      stationsbeskrivningen. Skjutgräns höger och vänster ska vara tydligt definierade och utmärkta.
    </p>

