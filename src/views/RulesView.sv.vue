    <a id="8" />
    <h2>8. Omskjutning (Re-shoots) och protestförfarande</h2>

    <a id="8.1" />
    <h3>8.1 Omskjutning (Re-shoot)</h3>
    <p id="8.1.1"><a href="#8.1.1"><span class="sk">8.1.1</span></a>
      Den tävlande har rätt till omskjutning (re-shoot) om:
    </p>
    <ol type="a">
      <li>Domaren (RO) vidrör eller på annat sätt stör den tävlande i sitt genomförande.</li>
      <li>En betydelsefull yttre störning sker som tydligt påverkar den tävlande i sitt genomförande.</li>
      <li>Den tävlande har inte haft möjlighet att få korrekt information om hur skjutstationen ska genomföras,
        alternativt den av funktionär förmedlade informationen har varit felaktig.</li>
      <li>Den tävlande stoppas i sitt genomförande av andra skäl än diskvalificerande säkerhetsöverträdelser.</li>
    </ol>
    <p id="8.1.2"><a href="#8.1.2"><span class="sk">8.1.2</span></a>
      Den tävlande ska genomföra omskjutning (re-shoot) om:
    </p>
    <ol type="a">
      <li>Ifyllt protokoll saknar registrerade träffar, missar, sluttid och/eller signatur. Om den tävlande själv har
        vägrat att skriva på protokollet så medger detta inte rätten till en omskjutning.</li>
      <li>Skjutstationens dukning och/eller förfarande har förändrats på ett sådant sätt som inte kan anses försumbart
        efter att den tävlande har genomfört skjutstationen.</li>
      <li>Det uppstår en felfunktion på skjutstationsdukningen som gör att skjutstationen inte kan genomföras enligt
        stationsbeskrivningen. Om ett reaktivt eller fragmenterande mål inte fungerar enligt plan ska genomförandet
        genast avbrytas.</li>
      <li>Den tävlande assisteras av någon person under sitt genomförande.</li>
      <li>Den registrerade sluttiden är uppenbart felaktig eller domaren (RO) uppfattar att den tävlandes registrerade
        poäng för skjutstationen är uppenbart felaktig.</li>
      <li>Domaren (RO) eller annan skjutstationsfunktionär på något sätt påverkar/vidrör ett reaktivt mål innan en
        kalibreringsbegäran är utförd eller om det reaktiva målet inte uppfyller kravet efter en kalibreringsbegäran.</li>
    </ol>
    <p>
      Vid oklara fall fattas beslutet om omskjutning (re-shoot) av banchef (RM). Om den tävlande vägrar att genomföra
      en re-shoot så registreras noll poäng för den tävlande som resultat för aktuell skjutstation.
    </p>
    <p id="8.1.3"><a href="#8.1.3"><span class="sk">8.1.3</span></a>
      Vid en eventuell omskjutning (re-shoot) räknas föregående resultat bort. Om den tävlande har rätt till en
      omskjutning ska denne inte tillåtas att markera tavlorna och ej heller ska domare (RO) avslöja föregående
      sluttid innan den tävlande tar beslut om att genomföra en omskjutning eller inte.
    </p>
    <p id="8.1.4"><a href="#8.1.4"><span class="sk">8.1.4</span></a>
      Om en omskjutning inte kan genomföras:
    </p>
    <ol type="a">
      <li>Och sluttid saknas i protokollet registreras noll poäng för genomförd skjutstation.</li>
      <li>Och protokollet saknar enstaka träffar/missar, fastställs protokollförd poäng.</li>
      <li>Och protokollet har fler träffar registrerade än vad som vid skjutstationen är möjligt, ska de högst
        poänggivande träffarna räknas.</li>
    </ol>
    <p id="8.1.5"><a href="#8.1.5"><span class="sk">8.1.5</span></a>
      En felfunktion i eller haveri av den tävlandes eldhandvapen under pågående genomförande leder inte till en
      omskjutning (re-shoot).
    </p>
    <p id="8.1.6"><a href="#8.1.6"><span class="sk">8.1.6</span></a>
      En hel skjutstation kan strykas om de tävlande inte har haft likartade förutsättningar för att genomföra den
      och detta kan ha haft en betydlig inverkan på slutgiltigt tävlingsresultat.
    </p>

    <a id="8.2" />
    <h3>8.2 Protestförfarande</h3>
    <p id="8.2.1"><a href="#8.2.1"><span class="sk">8.2.1</span></a>
      Om den tävlande är missnöjd med av domare (RO) fattat beslut kan denne klaga hos banchef (RM).
    </p>
    <p id="8.2.2"><a href="#8.2.2"><span class="sk">8.2.2</span></a>
      Om den tävlande är missnöjd med ett av banchefen (RM) fattat beslut kan denne lämna in en protest till
      tävlingsjuryn. En protest kan inte göras gällande säkerhetsöverträdelser. En tävlande kan dock lämna in en
      protest kring beslut som tas i motstridighet till gällande regelverk. Protest kan ej heller lämnas in kring
      frågor rörande markering av tavlor eller banchefens (RM) tolkning av protokollet.
    </p>
    <p id="8.2.3"><a href="#8.2.3"><span class="sk">8.2.3</span></a>
      En protest rörande alla frågor utom poängberäkning ska lämnas in senast två timmar efter berörd incident eller
      från det att den tävlande blev informerad av incidenten som denne vill protestera. I samtliga fall måste protest
      lämnas in innan tävlingen formellt avslutas så att en omskjutning (re-shoot) fortfarande är möjlig.
    </p>
    <p id="8.2.4"><a href="#8.2.4"><span class="sk">8.2.4</span></a>
      En tävlingsjury måste fatta beslut kring en protest innan slutgiltigt skjutstationsresultat är publicerat.
      Tävlingsjuryn måste skapa sig kännedom kring ärendet genom att höra den tävlande, domaren (RO), eventuella
      ögonvittnen och om möjligt ta del av filmat material rörande incidenten. Beslut ska fattas samma dag som
      protesten är inlämnad.
    </p>
    <p id="8.2.5"><a href="#8.2.5"><span class="sk">8.2.5</span></a>
      Tävlingsjuryn ska utses vid tävlingsgenomgången innan tävling. Tävlingsjuryns medlemmar består av:
      tävlingsledaren (MD) som är sammanhållande och är utan rösträtt samt tre utbildade TTS/SRA Domare (RO) som i
      övrigt inte tillhör tävlingsorganisationen. Tävlingsjuryns medlemmar ska ha utsedda suppleanter som kan träda
      in vid eventuella jävsituationer.
    </p>
    <p id="8.2.6"><a href="#8.2.6"><span class="sk">8.2.6</span></a>
      När en protest lämnas in till tävlingsjuryn vid tävlingar som ej anordnas av Försvarsmakten ska en protestavgift
      även betalas vid inlämnandet. Avgiftens storlek fastställs internationellt av RESUL och i Sverige av Svenska
      Pistolskytteförbundet (SPSF).
    </p>
    <p id="8.2.7"><a href="#8.2.7"><span class="sk">8.2.7</span></a>
      Om utfallet av tävlingsjuryns beslut är till den tävlandes fördel ska avgiften återbetalas till den tävlande. Om
      utfallet av tävlingsjuryns beslut är till den tävlandes nackdel tillfaller avgiften till RESUL eller i Sverige,
      Svenska Pistolskytteförbundet (SPSF).
    </p>
    <p id="8.2.8"><a href="#8.2.8"><span class="sk">8.2.8</span></a>
      Det är möjligt för andra förutom den aktuellt genomförande tävlande att lämna in protester kring en incident.
      Samma procedur som ovan är beskrivet gäller då.
    </p>
    <p id="8.2.9"><a href="#8.2.9"><span class="sk">8.2.9</span></a>
      I tävlingar under riksnivå och vid lokala tävlingar förekommer inte krav på en tävlingsjury. I dessa fall är
      tävlingsledaren (MD) beslutande organ. Det förekommer då ej heller någon avgift för att lämna in en protest.
    </p>

    <p class="approved">
      Dessa regler för TTS / SRA har granskats och godkänts av RESUL, FMIF (2023-05-16) och SPSF (2023-06-07).
    </p>

