    <a id="4" />
    <h2>4. Vid Skjutstation (Stage)</h2>

    <a id="4.1" />
    <h3>4.1 Övergripande</h3>
    <p id="4.1.1"><a href="#4.1.1"><span class="sk">4.1.1</span></a>
      Den tävlande ska följa alla styrningar givna av funktionärer, förutsatt att dessa styrningar faller innanför
      ramen för gällande säkerhetsregler.
    </p>
    <p id="4.1.2"><a href="#4.1.2"><span class="sk">4.1.2</span></a>
      Vid tävlingsverksamhet i Sverige nyttjas i huvudsak svenska och i Finland, finska. Vid internationella tävlingar
      nyttjas engelska. Tävlingsledaren (MD) beslutar vilket språk som ska gälla vid aktuell tävling. Domare (RO)
      beslutar om vilket språk denne nyttjar för att på bästa sätt ge kommandon till den tävlande. Alla kommandon ska
      artikuleras och ges med tydlig stämma.
    </p>
    <p id="4.1.3"><a href="#4.1.3"><span class="sk">4.1.3</span></a>
      Före genomförandet av skjutstation påbörjas ska en stationsbeskrivning ges till de tävlande. Stationsbeskrivningen
      ska vara tydlig och innehålla all nödvändig information för att den tävlande ska kunna genomföra skjutstationen
      på ett säkert sätt. Den muntliga stationsbeskrivningen ska minst innehålla:
    </p>
    <ol type="a">
      <li>Vid skjutstationen nyttjade eldhandvapen.</li>
      <li>Antal mål, målbeskrivning inklusive mål som ej ska bekämpas samt poängräkning.</li>
      <li>Avstånd till närmsta och bortersta mål.</li>
      <li>Minimal ammunitionsåtgång.</li>
      <li>Maxpoäng.</li>
      <li>Pekvinklar och skjutgränser.</li>
      <li>Placering av hanterings- och uppehållsområde.</li>
      <li>Spärr- och begränsningslinjer.</li>
      <li>Tillåten tid för att torröva skjutstationen (walktrough) efter att stationsbeskrivning är given.</li>
    </ol>
    <p>Utöver detta kan en stationsbeskrivning vid behov innehålla följande information:</p>
    <ol type="a" start="10">
      <li>Särskilda ammunitionsbegränsningar.</li>
      <li>Poängräkning för ej skjutande moment.</li>
      <li>Policy för poäng och tidräkning om annan än obegränsad (unlimited) gäller.</li>
      <li>Hur tidräkning går till om det avviker från tidräkning från startsignal till sista skott.</li>
      <li>Maximal tillåtna tid för genomförandet av skjutstationen.</li>
      <li>Startposition om annan än stående grundställning.</li>
      <li>Vapnets och övrig utrustnings status vid start.</li>
      <li>Särskilda begränsningar.</li>
      <li>Särskilda moment som ger straffpoäng.</li>
      <li>Särskilda säkerhetsrelaterade bestämmelser för skjutstationen.</li>
    </ol>
    <p>Den skriftliga stationsbeskrivningen kan också innehålla följande:</p>
    <ol type="a" start="20">
      <li>Skjutstationens scenario.</li>
    </ol>
    <p id="4.1.4"><a href="#4.1.4"><span class="sk">4.1.4</span></a>
      Domaren (RO) måste alltid säkerställa att den tävlande har förstått de förhållanden som återgivits i
      stationsbeskrivningen. Om domaren (RO) uppfattar att den tävlande inte har intagit korrekt startposition innan
      påbörjat genomförande ska detta åtgärdas av domaren (RO) innan genomförandet får påbörjas. Om den tävlande
      förutser och påbörjar genomförandet innan startsignal har ljudit ska den tävlandes genomförande avbrytas och
      skjutstationen ska skjutas om (re-shoot).
    </p>
    <p id="4.1.5"><a href="#4.1.5"><span class="sk">4.1.5</span></a>
      Den tävlande är själv ansvarig för att tillse att genomförandet påbörjas med korrekt mängd ammunition laddat i
      vapnet när sådana restriktioner är givna för skjutstationen. Varje extra patron utöver tillåtna mängd ger
      straffpoäng.
    </p>
    <p id="4.1.6"><a href="#4.1.6"><span class="sk">4.1.6</span></a>
      Om en tävlande i militär standard division har brister i sin minsta tillåtna utrustning tillåts denne att få
      åtgärda detta, alternativt flyttas till öppen (Open) division.
    </p>
    <p id="4.1.7"><a href="#4.1.7"><span class="sk">4.1.7</span></a>
      Att ta siktbild efter att Magasin i / Ladda är kommenderat är tillåtet men då endast mot ett enstaka mål.
    </p>
    <p id="4.1.8"><a href="#4.1.8"><span class="sk">4.1.8</span></a>
      Domaren (RO) måste förhålla sig i förflyttningen så att denne inte stör den tävlande i dennes genomförande.
      Domaren (RO) måste kunna ha full uppsikt över den tävlande under hela genomförandet.
    </p>
    <p id="4.1.9"><a href="#4.1.9"><span class="sk">4.1.9</span></a>
      Om den tävlande inte är startklar när det är dennes tur att genomföra kan domaren (RO) ändra skjutordningen på
      de tävlande.
    </p>
    <p id="4.1.10"><a href="#4.1.10"><span class="sk">4.1.10</span></a>
      Den tävlande kan under särskilda förhållanden efter tillåtelse från domare (RO) få kontrollskjuta sitt vapen för
      att kontrollera funktionalitet eller för att åtgärda ett eldavbrott. Dessa skott får ej avfyras mot de mål som
      ingår i genomförandet utan endast mot för ändamålet utplacerade mål eller mot anvisad vall som ej ingår i
      tävlingen.
    </p>

    <a id="4.2" />
    <h3>4.2 Nyttjade kommandon och genomförande av skjutstation</h3>
    <p id="4.2.1"><a href="#4.2.1"><span class="sk">4.2.1</span></a>
      Följande grundläggande kommandon är de som nyttjas och i nedan angiven ordning. Efter beskrivet svenskt kommando
      återges detsamma på finska och engelska inom parentes.
    </p>

    <p class="range-command">UTRYM STATIONEN (AMMUTAAN — RANGE IS CLOSED)</p>
    <p>Kommandot ges i syfte att utrymma skjutstationen. All personal utom den tävlande och domaren (RO) ska utan
      dröjsmål förflytta sig till uppehållsområdet om inte annat är särskilt angivet av domare (RO). Domaren (RO) ska
      säkerställa att det är säkert att påbörja genomförandet samt att hörselskydd och skyddsglasögon är applicerade.</p>

    <p class="range-command">NÄSTA SKYTT (SEURAAVA AMPUJA — NEXT SHOOTER)</p>
    <p>Nästa tävlande ansluter till domaren (RO) med anbefalld utrustning medförd och buren enligt anvisningar och
      inväntar vidare kommandon.</p>

    <p class="range-command">HAR SKYTTEN NÅGRA FRÅGOR (ONKO KYSYTTÄVÄÄ — ANY QUESTIONS)</p>
    <p>Domaren (RO) säkerställer att den tävlande har förstått stationsbeskrivningen. Den tävlande kan här ställa
      eventuellt uppkomna frågor om genomförandet.</p>

    <p class="range-command">MAGASIN I (LIPAS KIINNITÄ — INSERT MAGAZINE / LOAD MAGAZINE)</p>
    <p>Den tävlande för in ett magasin i vapnet men genomför ingen laddrörelse. Detta kommando ges endast vid
      genomföranden som startar med ett tomt patronläge. Om vapnet ej är magasinsmatat så nyttjas adekvat laddprocedur
      för att uppnå ett motsvarande läge. Mekanism får ej vara öppen och/eller uppspänd vid start.</p>

    <p class="range-command">LADDA (LATAA JA VARMISTA — LOAD AND MAKE READY)</p>
    <p>Vapnet laddas med en patron i patronläget och vapnet säkras om möjligt, alternativt slackas vapen med
      ”decocker” som inte har annan typ av säkring. Om slackning kräver att avtryckaren påverkas ska det vidtalas till
      domaren (RO). Om fler än ett vapen ska nyttjas av den tävlande vid en skjutstation och ett av dessa vapen är en
      pistol som ej heller nyttjas som vapen vid start av skjutstationen ska pistolen ej vara laddad med en patron i
      loppet.</p>

    <p class="range-command">STARTPOSITION (ALOITUSASENTOON — STARTING POSITION)</p>
    <p>Om inget annat anges i stationsbeskrivningen så startar den tävlande i grundställning vilket är:</p>
    <ul>
      <li>Enhandsvapen: ståendes vänd mot målet, händerna längs sidorna på kroppen, pistolen hölstrad, om möjligt ska
        hölstret vara låst.</li>
      <li>Tvåhandsvapen: vapnet ska bäras eller hållas hängande på bröstet med mynningen nedåt, med eller utan rem runt
        kroppen och med axelstöd eller kolv vid axeln. Rem avpassad så att vapnet kan föras upp i färdig- eller
        anläggningsställning. Skjuthanden fattar om pistolgreppet, stödhanden om handskyddet. Avtryckarfingret hålls
        utanför varbygeln. Saknas handskydd ska stödhanden fatta vapnet framför magasinsstyrningen. Förutom burna
        handskar får ingen övrig utrustning såsom t.ex. magasin hållas i händerna.</li>
      <li>Båda fötterna ska vara hitom spärrlinjen.</li>
    </ul>

    <p class="range-command">FÄRDIGSTÄLLNING / ANLÄGGNING (VARMISTIN POIS — SAFETY OFF)</p>
    <p>Detta kommando nyttjas om stationsbeskrivningen anger att genomförandet börjar med ett osäkrat vapen, antingen i
      färdigställning eller anläggning. Den tävlande har före detta kommando ges intagit rätt startposition enligt
      stationsbeskrivning med sitt vapen säkrat.</p>

    <p class="range-command">ÄR SKYTTEN REDO (ONKO AMPUJA VALMIS — IS SHOOTER READY)</p>
    <p>Detta kommando nyttjas när den tävlande intagit anbefalld startposition enligt ovan. Den tävlande bekräftar till
      domaren (RO) att denne är redo genom att anmäla ”REDO”.</p>

    <p class="range-command">FÄRDIGA (HUOMIO — STAND BY)</p>
    <p>Domaren startar genomförandet genom att starta skjuttimern som 2–5 sekunder senare ger ifrån sig en startsignal.</p>

    <p class="range-command">ELD (TULTA — FIRE)</p>
    <p>Om inte ovan nämnda metod med startsignal från skjuttimer nyttjas kan kommandot eld ges av domare (RO) för att
      starta genomförandet. När den tävlande självmant har avslutat sitt genomförande eller genomförandet avbryts av
      angiven slutsignal, står den tävlande still och inväntar vidare kommandon från domare (RO).</p>

    <p class="range-command">ÄR SKYTTEN KLAR (OLETKO LOPETTANUT — ARE YOU FINISHED)</p>
    <p>Frågan ställs av domaren (RO) till den tävlande när den tävlande verkar vara klar med sitt genomförande. Om
      skjutstationen inte har en definierad slutsignal må den tävlande fortsätta genomförandet om så önskas till och
      med att sluttid är uppnådd. Tidräkning pågår då till och med sista skjutna skott alternativt sluttid är uppnådd.
      Den tävlande besvarar frågan om denne är klar med att svara ”JA” eller på annat sätt tydligt indikera att denne
      är klar.</p>

    <p class="range-command">PATRON UR, SPÄNN UPP FÖR VISITATION (LIPAS IRTI, LIIKKUVAT TAAKSE / PATRUUNAT POIS,
      LIIKKUVAT TAAKSE — UNLOAD AND SHOW CLEAR)</p>
    <p>Den tävlande kontrollerar att vapnet är säkrat. Därefter tar den tävlande ur magasinet alternativt plundrar
      vapnet om det inte är magasinsmatat. Den tävlande spänner upp vapnet och låter eventuell patron falla till
      marken. Den tävlande kontrollerar att inget magasin finns fäst vid vapnet samt att ingen patron finns i
      patronläget genom att se in och känna med ett finger eller kontrolldon genom utkastaren och ut mot
      magasinsstyrningen samt in mot patronläget. Den tävlande släpper därefter fram mekanismen och för in stödhandens
      tumme i magasinsstyrningen och blindavfyrar därefter vapnet i skjutriktningen. Därefter spänner den tävlande upp
      mekanismen och låter domaren (RO) visitera patronläget. Vid tävling utomlands visiterar domaren (RO) patronläget
      INNAN den tävlande blindavfyrar sitt vapen. Eventuella skott som avfyras efter att kommandot PATRON UR är givet
      räknas som vådaskott och hanteras därefter enligt regelboken. Den tävlande är alltid ensamt ansvarig för att
      tillse att sitt vapen är oladdat och utan magasin efter ett genomförande har avslutats.</p>

    <p class="range-command">BLINDAVFYRA (TYHJÄ LAUKAUS — HAMMER DOWN)</p>
    <p>Efter att domare (RO) har visiterat patronläget kommenderas BLINDAVFYRA och den tävlande blindavfyrar då sitt
      vapen i skjutriktningen. Om vapnet har magasinsstyrd säkring så släpps mekanismen fram och ett tomt magasin förs
      in i vapnet innan det blindavfyras i skjutriktningen. Efter genomförd blindavfyrning avlägsnas brukade tomma
      magasin.</p>

    <p class="range-command">HÖLSTRA / PIPA UPP (ASE KOTELOON / ASE KANTOASENTOON — HOLSTER / MUZZLE UP)</p>
    <p>Efter att detta kommando är givet och efterföljt är den tävlande klar att lämna skjutstationen. Vid kommando
      HÖLSTRA så hölstrar den tävlande sitt enhandsvapen på ett säkert sätt och domaren (RO) ger därefter den tävlande
      tillåtelse att lämna skjutstationen. Vid kommando PIPA UPP tillser den tävlande att dennes brukade tvåhandsvapen
      bärs på ett sådant sätt att pipan är riktad rakt upp alternativt rakt ner. Den tävlande kan alternativt packa
      ner sitt tvåhandsvapen i ett fodral eller därför anpassad låda och kan då därefter förflytta vapnet med pipan
      riktad i horisontalplanet.</p>

    <p class="range-command">MARKERA (TAULUILLE — RANGE IS CLEAR)</p>
    <p>Den tävlande tillsammans med domare (RO) och protokollförare (SK) kontrollerar träffresultat. Klistrare
      medföljer bakom och klistrar tavlan efter att de har fått klartecken från protokollföraren (SK). Nästa tävlande
      har då tillträde till skjutstationen och kan ställa upp inför startposition. Övriga tävlande kvarstannar i
      uppehållsområde.</p>

    <p class="range-command">AVBRYT (SEIS — STOP)</p>
    <p>Vid kommando AVBRYT avbryter den tävlande all verksamhet och står still. Vapnet ska vara riktat innanför
      pekvinklarna, fingret ska vara utanför varbygeln och den tävlande inväntar vidare instruktioner från domaren
      (RO). ALLA har rätten att nyttja detta kommando då potentiell farlig situation uppstår. Detta gäller även om
      domare (RO) inte har uppfattat samma situation. Överträdelser mot kommandot AVBRYT leder till diskvalificering
      från tävlingen.</p>

    <p class="range-command">MANÖVER (VARO — WATCH OUT)</p>
    <p>Kommandot MANÖVER nyttjas för att göra den tävlande medveten på att dennes uppträdande tangerar att överträda
      säkerhetsreglerna. Exempel på detta är:</p>
    <ul>
      <li>Den tävlande är på väg att avfyra sitt vapen mot stålmål på ett avstånd som är närmare än minsta tillåtna
        skjutavstånd för stålmål.</li>
      <li>Den tävlande är på väg att föra sitt vapen utanför pekvinklarna.</li>
      <li>Den tävlande avfyrar sitt vapen på ett sådant sätt att dukade barrikader träffas och kan orsaka splitter och
        rikoschetter.</li>
    </ul>
    <p>Om den tävlande uppmärksammar kommandot MANÖVER och ändrar sitt beteende så fortsätter genomförandet enligt
      tidigare givna instruktioner. Om den tävlande inte hörsammar kommandot MANÖVER och fortsätter genomförandet med
      ett beteende som påverkar säkerheten så ska kommandot AVBRYT kommenderas av domare (RO) och genomförandet
      avbryts.</p>

    <p id="4.2.2"><a href="#4.2.2"><span class="sk">4.2.2</span></a>
      Om skjutstationen innefattar flertalet vapentyper ska kommandon rörande vapnet ges specifikt för respektive
      vapentyp. Exempel: för pistol, MAGASIN I. För karbin, LADDA.
    </p>
    <p id="4.2.3"><a href="#4.2.3"><span class="sk">4.2.3</span></a>
      Vid behov kan ytterligare kommandon nyttjas vid skjutstationen. Exempelvis: TA PLATS I FORDONET. Om sådana
      kommandon förekommer ska dessa förmedlas till de tävlande i stationsbeskrivningen samt repeteras för den
      tävlande vid startposition innan HAR SKYTTEN NÅGRA FRÅGOR kommenderas.
    </p>

